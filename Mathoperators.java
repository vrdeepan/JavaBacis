public class Mathoperators {
    public static void main(String[] args) {
        double a = 10;
        double b = 20;
        double c;
        c = a + b;
        System.out.println(c);
        c = b - a;
        System.out.println(c);
        c = b * a;
        System.out.println(c);
        c = b / a;
        System.out.println(c);
        c = b % a;
        System.out.println(c);
    }
}
